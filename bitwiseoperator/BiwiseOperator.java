
package bitwiseoperator;


public class BiwiseOperator {
    
    public static void main(String[] args) {
        
        int a=8;
        int b=4;
        
        int c;
        
        c=a&b;
        System.out.println("a$b ="+c);
        
        c=a|b;
        System.out.println("a|b ="+c);
        
        c=a^b;
        System.out.println("a^b ="+c);
        
        
    }
    
}
